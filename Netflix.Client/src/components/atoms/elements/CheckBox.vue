/***********************************************************************************************************/
/*                                             I M P O R T                                                 */
/***********************************************************************************************************/
    import { defineProps, toRefs } from 'vue'

/***********************************************************************************************************/
/*                                               D A T A                                                   */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                            C O M P U T E D                                              */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                              P R O P S                                                  */
/***********************************************************************************************************/
    const props = withDefaults(defineProps<{ checked: boolean, chkId: string }>(), { checked: false, chkId: 'allChk' });
    const { checked, chkId } = toRefs(props);

/***********************************************************************************************************/
/*                                           L I F E C Y C L E                                             */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                             M E T H O D                                                 */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                               E M I T                                                   */
/***********************************************************************************************************/
    const emit = defineEmits<{ ChangeValue: [value: boolean] }>();

/***********************************************************************************************************/
/*                                              W A T C H                                                  */
/***********************************************************************************************************/

</script>