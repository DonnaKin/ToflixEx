import { defineStore } from "pinia";
import { reactive } from "vue";
import api from '@/api/cotents'

export const ContentsStore = defineStore('contentsStore', () => {
/***********************************************************************************************************/
/*                                             S T A T E                                                   */
/***********************************************************************************************************/
    const contents = reactive<Array<Content>>([]);

/***********************************************************************************************************/
/*                                            A C T I O N                                                  */
/***********************************************************************************************************/
    async function LoadData() {
        await api.GetContentItems().then((res: CommonResponse<Array<Content>>) => {
            if (res.errorCode == "0000") {
                Object.assign(contents, res.data);
            }
        })
    }

/***********************************************************************************************************/
/*                                            G E T T E R                                                  */
/***********************************************************************************************************/


/***********************************************************************************************************/
/*                                            R E T U R N                                                  */
/***********************************************************************************************************/
    return { contents, LoadData }

})